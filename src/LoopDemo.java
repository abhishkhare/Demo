
public class LoopDemo {
	public static void main(String[] args) {
		
		//for, while, do while, for-each 
		
		for (int i=0; i<5;i++)
		System.out.println("Hello");
		//while 
		int j=1;
		while (j<=3)
		{
			System.out.println("Bye");
		    j++;
		}
		
		int k=4; 
		do
		{
			System.out.println("Goodbye");
		} 
		while(k<3);
	}
}
